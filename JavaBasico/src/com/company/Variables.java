package com.company;

public class Variables {
    public static void main(String[] args) {

        int variable1 = 1;
        long variable2 = 22222;
        double variable3 = 1.1;
        boolean variable4 = true;
        String variable5 = "hola mundo";

        System.out.println("variable1: " + variable1);
        System.out.println("variable2: " + variable2);
        System.out.println("variable3: " + variable3);
        System.out.println("variable4: " + variable4);
        System.out.println("variable5: " + variable5);
    }
}
